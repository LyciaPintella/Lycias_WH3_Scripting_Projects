     local pool = cror_list[i]
         .merc_pool                                -- NEW found in "ui_mercenary_recruitment_infos_tables" table, or in "mercenary_pools_tables" beneath UI recruitment info
     local unit_key = cror_list[i]
     .unit                                         -- Key of unit to add to the mercenary pool, from the main_units table
     local unit_count = cror_list[i].count         -- Number of units to add to the mercenary pool
     local rcp = cror_list[i].rcp                  -- Replenishment chance, as a percentage
     local munits = cror_list[i]
         .munits                                   -- The maximum number of units of the supplied type that the pool is allowed to contain.
     local murpt = cror_list[i]
         .murpt                                    -- The maximum number of units of the supplied type that may be added by replenishment per-turn
     local frr = cror_list[i]
         .frr                                      -- (may be empty) The key of the faction who can actually recruit the units, from the factions database table
     local srr = cror_list[i]
         .srr                                      -- (may be empty) The key of the subculture who can actually recruit the units, from the cultures_subcultures database table
     local trr = cror_list[i]
         .trr                                      -- (may be empty) The key of a technology that must be researched in order to recruit the units, from the technologies database table
     local replen = cror_list[i].replen            -- Allow replenishment of partial units
     local merc_group_key = cror_list[i]
         .merc_group                               -- key used in mercenary_unit_groups_tables can most of the time be the same has main_unit key
     -- Adding the listed unit to the listed faction in the above table
     cm:add_unit_to_faction_mercenary_pool(
          faction,
          unit_key,
          pool,
          unit_count,
          rcp,
          munits,
          murpt,
          frr,
          srr,
          trr,
          replen,
          merc_group_key
     )

     -- Debug message for log
     out(
          "CROR add_all_custom_ror_JM_Chorf 476: adding the custom ror unit "
          .. unit_key
          .. " to "
          .. faction_name
     )
